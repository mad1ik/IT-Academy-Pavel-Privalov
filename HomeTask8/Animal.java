package HomeTask8;

public class Animal {
    private String age;
    private String nick;

    public Animal(String age, String nick) {
        this.age = age;
        this.nick = nick;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    static int RandomString(int stringLength){
        return stringLength;
    }
}
